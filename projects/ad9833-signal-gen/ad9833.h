/**
 * ad9833.h — AD9833 寄存器定义
 * 对照手册: AD9833 中文资料 第 8-9 页
 */

#ifndef __AD9833_H
#define __AD9833_H

#include "stm32f1xx_hal.h"

/* ============================================================
 * 第一部分: 硬件参数
 * ============================================================ */

/* AD9833 的时钟源频率 (看你的模块原理图, 晶振是 25MHz) */
#define AD9833_MCLK    25000000U

/*
 * 2^28 = 268435456
 * AD9833 内部有一个 28 位的累加器, 频率分辨率 = MCLK / 2^28 ≈ 0.1Hz
 */
#define AD9833_2POW28  268435456.0f

/* ============================================================
 * 第二部分: 16-bit 命令的前两位 (D15-D14)
 * 手册第 8 页 — 表 8: 控制寄存器
 * ============================================================ */

/*
 * 这两个 bit 告诉 AD9833: "接下来我要写什么"
 *
 * 00 → 控制寄存器 (设置波形、复位、休眠等)
 * 01 → FREQ0 寄存器 (设置频率)
 * 10 → FREQ1 寄存器 (设置频率)
 * 11 → PHASE 寄存器 (设置相位偏移)
 */

#define CMD_CONTROL   0x0000   /* D15=0, D14=0 → 控制寄存器 */
#define CMD_FREQ0     0x4000   /* D15=0, D14=1 → 频率寄存器0 */
#define CMD_FREQ1     0x8000   /* D15=1, D14=0 → 频率寄存器1 */
#define CMD_PHASE0    0xC000   /* D15=1, D14=1 → 相位寄存器0 */
#define CMD_PHASE1    0xE000   /* D15=1, D14=1 + D13=1 → 相位寄存器1 */

/* ============================================================
 * 第三部分: 控制寄存器各 BIT 的含义 (D13-D0)
 * 手册第 8-9 页
 * ============================================================ */

/*
 * D13: B28
 *   = 1 → "我要连续写两次, 凑齐28位频率值" (推荐! 频率不会在中间跳变)
 *   = 0 → "我自己分两次写, 用 HLB 位控制写高还是写低"
 *
 * 我们用 B28=1, 最简单。AD9833 收到连续两个 16-bit 后自动拼成 28-bit。
 */
#define BIT_B28        (1 << 13)

/*
 * D12: HLB (只在 B28=0 时有意义)
 *   = 0 → 写频率字的低14位
 *   = 1 → 写频率字的高14位
 *
 * 我们用 B28=1 所以不需要这个, 保留在这里供参考。
 */
#define BIT_HLB        (1 << 12)

/*
 * D11: FSELECT
 *   = 0 → 输出频率 = FREQ0 里的值
 *   = 1 → 输出频率 = FREQ1 里的值
 *
 * 通过切换这个 bit, 可以实现两个频率之间快速跳变(Frequency Shift Keying, FSK)
 */
#define BIT_FSELECT    (1 << 11)

/*
 * D10: PSELECT
 *   = 0 → 相位偏移 = PHASE0 里的值
 *   = 1 → 相位偏移 = PHASE1 里的值
 */
#define BIT_PSELECT    (1 << 10)

/*
 * D9: PIN/SW
 *   = 1 → 用外部引脚控制 (我们没接)
 *   = 0 → 用软件控制 ← 我们选这个
 */
#define BIT_PIN_SW     (1 << 9)

/*
 * D8: RESET
 *   = 1 → AD9833 复位, 输出恒定为中间电平 (没有波形)
 *   = 0 → 正常输出
 *
 * 用法:
 *   1. 配置波形和频率时, 先置 RESET=1 (让输出安静)
 *   2. 配完之后, 置 RESET=0 (波形出来)
 * 这样可以避免配置过程中产生毛刺。
 */
#define BIT_RESET      (1 << 8)

/*
 * D7: SLEEP1
 *   = 1 → 关闭内部 MCLK (省电模式)
 *   = 0 → 正常
 */
#define BIT_SLEEP1     (1 << 7)

/*
 * D6: SLEEP12
 *   = 1 → 关闭 DAC (省电模式)
 *   = 0 → 正常
 */
#define BIT_SLEEP12    (1 << 6)

/*
 * D5: OPBITEN
 * 这个 bit 控制 VOUT 引脚输出什么东西:
 *   = 0 → VOUT 输出 DAC 产生的正弦波/三角波
 *   = 1 → VOUT 输出 DAC 数据的最高位 (一个方波)
 *
 * 生成方波时需要置 1!
 */
#define BIT_OPBITEN    (1 << 5)

/*
 * D4: SIGN_PIB
 *   (只在 OPBITEN=1 时有意义, 生成正弦/三角波时忽略)
 *   配合 OPBITEN 和 DIV2 使用
 */
#define BIT_SIGN_PIB   (1 << 4)

/*
 * D3: DIV2
 *   (只在 OPBITEN=1 时有意义)
 *   = 0 → 方波频率 = 设定频率
 *   = 1 → 方波频率 = 设定频率 / 2
 *
 * 为什么要除2? AD9833 方波输出的占空比不是严格的50:50,
 * 加 DIV2 之后占空比会好很多。所以生成方波时 OPBITEN=1, DIV2=1。
 * 同时你要把设定的频率 ×2 来补偿这个除2。
 */
#define BIT_DIV2       (1 << 3)

/* D2: 保留, 写 0 */

/*
 * D1: MODE
 *   = 0 → 输出正弦波 (DAC 产生)
 *   = 1 → 输出三角波 (DAC 产生)
 *
 * 这个 bit 只在 OPBITEN=0 时有效。
 * 因为方波用的是 OPBITEN=1 那套机制, 跟 MODE 无关。
 */
#define BIT_MODE       (1 << 1)

/* D0: 保留, 写 0 */

/* ============================================================
 * 第四部分: 预设的控制字组合 (方便使用)
 * ============================================================ */

/*
 * 正弦波: B28=1 (连续28bit频率写入), 软件控制, MODE=0
 * 三角波: B28=1, 软件控制, MODE=1
 * 方波:   B28=1, 软件控制, OPBITEN=1, DIV2=1
 *
 * 注意: 这里的 RESET=1 (先复位, 配完频率之后再清零)
 */

/* 复位 + B28 模式 (这是每次配频率之前的基础状态) */
#define CTRL_RESET_B28     (BIT_RESET | BIT_B28)

/* 在 CTRL_RESET_B28 基础上叠加的模式位 */
#define CTRL_SINE          (CTRL_RESET_B28 | 0x0000)          /* MODE=0 → 正弦 */
#define CTRL_TRIANGLE      (CTRL_RESET_B28 | BIT_MODE)        /* MODE=1 → 三角 */
#define CTRL_SQUARE        (CTRL_RESET_B28 | BIT_OPBITEN | BIT_DIV2) /* 方波 */

/* ============================================================
 * 第五部分: 函数声明
 * ============================================================ */

/*
 * 我们会写三个函数:
 *   1. AD9833_Init()      — 初始化
 *   2. AD9833_SetFreq()   — 设置频率
 *   3. AD9833_SetWave()   — 设置波形
 *
 * 先写头文件, 函数实现明天写。
 */

void AD9833_Init(void);
void AD9833_SetFreq(float freq_hz);
void AD9833_SetWave(uint16_t wave_ctrl);
void AD9833_WriteRegister(uint16_t data);

#endif /* __AD9833_H */
